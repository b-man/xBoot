#include <shell.h>
#include <stdio.h>
#include <string.h>

#include "cmds.h"

/**
 * query_command
 *
 * query a command and return its associated handle.
 */
cmd_handle_t query_command(const char *name)
{
	int i = 0;
	cmd_handle_t cmd = { NULL, NULL, NULL };

	while (commands[i].name != NULL) {
		if (strncmp(commands[i].name, name, strlen(name)) == 0)
			cmd = commands[i];
		++i;
	}

	return cmd;
}
